#include <stdlib.h>
#include <string.h>
#include <stdio.h>
#include <libft.h>

char	*ft_strncpy(char *dst, const char *src, size_t len)
{
	size_t i;

	i = 0;
	while (src[i] != '\0' && i <= len - 1)
	{
		dst[i] = src[i];
		i++;
	}
	return (dst);
}
