#include <unistd.h>

char	ft_strcpy(char *s2, const char *s1)
{
	int i;

	i = 0;
	while (s1[i])
	{
		s2[i] = s1[i];
		i++;
	}
	s2[i] = '\0';
	return (s2);
}
